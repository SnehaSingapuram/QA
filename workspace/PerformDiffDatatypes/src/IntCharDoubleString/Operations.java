package IntCharDoubleString;

public class Operations {

	public static void main(String[] args) 
	{
		int a;
		double b,e;
		char c;
		String d,f,g,h;
		
		a= 200;
		b= 100.1;
		c= 'A';
		d= "Sneha";
		g= "Anvesh";
		
		//Addition of one int value and one double value
		e= a+b;
		
		//Adding two string values
		f= d+g;
		
		//Adding a char and a string
		h= d+c;
		
	System.out.println("Adding int value and double value is " +e);
	System.out.println(" Adding two string values " +f);
	System.out.println(" Value of char is " +c);
	System.out.println(" Adding a char and a string " +h);

	}

}


